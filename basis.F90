            locprim(ishell+1)= locprim(ishell)+1
            locbf(ishell+1) = locbf(ishell)+3
          enddo
!  D function
          do k= 0,2
            ishell= ishell+1
            ex(locprim(ishell)+1)= expd(ide(numatomic(iatom))+1+k)
            coeff(locprim(ishell)+1)= one
            mprim(ishell)= 1
            mtype(ishell)= 2
            locatom(ishell)= iatom
            locprim(ishell+1)= locprim(ishell)+1
            if(spher) then
              mbf(ishell)= 5
              locbf(ishell+1) = locbf(ishell)+5
            else
              mbf(ishell)= 6
              locbf(ishell+1) = locbf(ishell)+6
            endif
          enddo
!  F function
          do k= 0,1
            ishell= ishell+1
            ex(locprim(ishell)+1)= expf(ife(numatomic(iatom))+1+k)
            coeff(locprim(ishell)+1)= one
            mprim(ishell)= 1
            mtype(ishell)= 3
            locatom(ishell)= iatom
            locprim(ishell+1)= locprim(ishell)+1
            if(spher) then
              mbf(ishell)= 7
              locbf(ishell+1) = locbf(ishell)+7
            else
              mbf(ishell)= 10
              locbf(ishell+1) = locbf(ishell)+10
            endif
          enddo
!  G function
          ishell= ishell+1
          ex(locprim(ishell)+1)= expg(ige(numatomic(iatom))+1)
          coeff(locprim(ishell)+1)= one
          mprim(ishell)= 1
          mtype(ishell)= 4
          locatom(ishell)= iatom
          locprim(ishell+1)= locprim(ishell)+1
          if(spher) then
            mbf(ishell)= 9
            locbf(ishell+1) = locbf(ishell)+9
          else
            mbf(ishell)= 15
            locbf(ishell+1) = locbf(ishell)+15
          endif
      endselect
!
      if(ishell > mxshell) then
        write(iout,'(" Error! The number of basis shells exceeds mxshel",i6,".")')mxshell
        call iabort
      endif
      if(locprim(ishell+1) > mxprim ) then
        write(iout,'(" Error! The number of primitive basis functions exceeds mxshel", &
&             i6,".")')mxprim
        call iabort
      endif
      if(locbf(ishell+1) > mxao ) then
        write(iout,'(" Error! The number of basis functions exceeds mxshel",i6,".")')mxao
        call iabort
      endif
      return
end

































