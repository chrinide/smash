!----------------------
  subroutine setbasis
!----------------------
!
! Driver of setting basis functions
!
      use iofile,only : iout
      use procpar, only : master, parallel, MPI_COMM_WORLD
      use molecule, only : natom
      use basis, only : func, locprim, locbf, locatom, mprim, mbf, mtype, &
&                       nshell, nao, nprim, ex, coeff, coeffinp
      implicit none
      integer :: ishell, iatom, i
!
      if(master) then
        locprim(1)=0
        locbf(1)=0
        ishell= 0
!
        if(func == 'STO-3G') then
          do iatom = 1, natom
            call bssto3g(iatom,ishell)
          enddo
        elseif(func == '6-31G') then
          do iatom = 1, natom
            call bs631g(iatom,ishell)
          enddo
        elseif(func == '6-31G(D)') then
          do iatom = 1, natom
            call bs631gd(iatom,ishell)
          enddo
        elseif(func == 'CC-PVDZ') then
          do iatom = 1, natom
            call bsccpvdz(iatom,ishell)
          enddo
        elseif(func == 'CC-PVTZ') then
          do iatom = 1, natom
            call bsccpvtz(iatom,ishell)
          enddo
        elseif(func == 'CC-PVQZ') then
          do iatom = 1, natom
            call bsccpvqz(iatom,ishell)
          enddo
!ISHIMURA
!       elseif(func == 'GEN') then
        else
          write(iout,'(" Error! Basis set is not set.")')
          call iabort
        endif
!
        nshell= ishell
      endif
!
      if(parallel) then
        call para_bcast(nshell,1,"I",0,MPI_COMM_WORLD)
        call para_bcast(locprim,nshell+1,"D",0,MPI_COMM_WORLD)
        call para_bcast(locbf  ,nshell+1,"D",0,MPI_COMM_WORLD)
        call para_bcast(locatom,nshell+1,"D",0,MPI_COMM_WORLD)
      endif
!
      nao= locbf(nshell+1)
      nprim= locprim(nshell+1)
!
      if(parallel) then
        call para_bcast(ex   ,nprim,"D",0,MPI_COMM_WORLD)
        call para_bcast(coeff,nprim,"D",0,MPI_COMM_WORLD)
        call para_bcast(mprim,nshell,"D",0,MPI_COMM_WORLD)
        call para_bcast(mbf  ,nshell,"D",0,MPI_COMM_WORLD)
        call para_bcast(mtype,nshell,"D",0,MPI_COMM_WORLD)
      endif
!
      do i= 1,nprim
        coeffinp(i)= coeff(i)
      enddo
!
      call bsnrmlz
      return
end


!---------------------
  subroutine bsnrmlz
!---------------------
!
! Normalize basis functions
!
      use basis, only : nshell, mtype, ex, coeff, locprim, mprim
      implicit none
      integer :: ishell
!
!$OMP parallel do
      do ishell= 1,nshell
        call bsnor(ishell,ex,coeff,locprim,mprim,mtype)
      enddo
!$OMP end parallel do
      return
end


!--------------------------------------------------------
  subroutine bsnor(ishell,ex,coeff,locprim,mprim,mtype)
!--------------------------------------------------------
!
! Normalize basis functions
!
      use param, only : mxprim, mxshell
      implicit none
      integer,intent(in) :: ishell, locprim(mxshell+1), mprim(mxshell), mtype(mxshell)
      integer :: iloc, iprim, jprim, nprimi, nangi
      real(8),parameter :: zero=0.0D+00, one=1.0D+00, two=2.0D+00
      real(8),parameter :: pi32=5.568327996831708D+00   ! pi32=pi**(3/2)
      real(8),parameter :: pii32=0.1795871221251666D+00 ! pii32=pi**(-3/2)
      real(8),intent(in) :: ex(mxprim)
      real(8),intent(inout) :: coeff(mxprim)
      real(8) :: exij, fac1, fac2
      real(8) :: factor(0:6)=(/1.0D+0,1.0D+0,3.0D+0,15.0D+0,105.0D+0,945.0D+0,10395.0D+0/)
!
! Normalize primitive functions
!
      iloc= locprim(ishell)
      nprimi= mprim(ishell)
      nangi= mtype(ishell)
      do iprim= iloc+1,iloc+nprimi
        exij= ex(iprim)*two
        fac1= pii32*exij*sqrt(exij)*(two*exij)**nangi
        coeff(iprim)= coeff(iprim)*sqrt(fac1/factor(nangi))
      enddo
!
! Normalize contracted functions
!
      fac2= zero
      do iprim= iloc+1,iloc+nprimi
        do jprim= iloc+1,iprim-1
          exij= ex(iprim)+ex(jprim)
          fac1= exij*sqrt(exij)*(two*exij)**nangi
          fac2= fac2+coeff(iprim)*coeff(jprim)*two*factor(nangi)/fac1
        enddo
        exij= ex(iprim)*two
        fac1= exij*sqrt(exij)*(two*exij)**nangi
        fac2= fac2+coeff(iprim)*coeff(iprim)*factor(nangi)/fac1
      enddo
      fac2= one/sqrt(fac2*pi32)
      do iprim= iloc+1,iloc+nprimi
        coeff(iprim)= coeff(iprim)*fac2
      enddo
      return
end


!-----------------------
  subroutine bsnrmlz_g
!-----------------------
!
! Normalize basis functions for guess calculation
!
      use guess, only : nshell_g, mtype_g, ex_g, coeff_g, locprim_g, mprim_g
      implicit none
      integer :: ishell
!
!$OMP parallel do
      do ishell= 1,nshell_g
        call bsnor(ishell,ex_g,coeff_g,locprim_g,mprim_g,mtype_g)
      enddo
!$OMP end parallel do
      return
end


