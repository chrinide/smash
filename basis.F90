          locprim(ishell+1)= locprim(ishell)+nprim
          locbf(ishell+1) = locbf(ishell)+3
!
          ishell= ishell+1
          do j= 1,1
            ex(locprim(ishell)+j)= expp(ipe(numatomic(iatom))+j+nprim)
            coeff(locprim(ishell)+j)= coeffp(ipc(numatomic(iatom))+j+nprim*2)
          enddo
          mprim(ishell)= 1
          mbf(ishell)= 3
          mtype(ishell)= 1
          locatom(ishell)= iatom
          locprim(ishell+1)= locprim(ishell)+1
          locbf(ishell+1) = locbf(ishell)+3
!
          ishell= ishell+1
          do j= 1,1
            ex(locprim(ishell)+j)= expp(ipe(numatomic(iatom))+j+nprim+1)
            coeff(locprim(ishell)+j)= coeffp(ipc(numatomic(iatom))+j+nprim*2+1)
          enddo
          mprim(ishell)= 1
          mbf(ishell)= 3
          mtype(ishell)= 1
          locatom(ishell)= iatom
          locprim(ishell+1)= locprim(ishell)+1
          locbf(ishell+1) = locbf(ishell)+3
!  D function
          ishell= ishell+1
          do j= 1,1
            ex(locprim(ishell)+j)= expd(ide(numatomic(iatom))+j)
            coeff(locprim(ishell)+j)= one
          enddo
          mprim(ishell)= 1
          mtype(ishell)= 2
          locatom(ishell)= iatom
          locprim(ishell+1)= locprim(ishell)+1
          if(spher) then
            mbf(ishell)= 5
            locbf(ishell+1) = locbf(ishell)+5
          else
            mbf(ishell)= 6
            locbf(ishell+1) = locbf(ishell)+6
          endif
!
          ishell= ishell+1
          do j= 1,1
            ex(locprim(ishell)+j)= expd(ide(numatomic(iatom))+j+1)
            coeff(locprim(ishell)+j)= one
          enddo
          mprim(ishell)= 1
          mtype(ishell)= 2
          locatom(ishell)= iatom
          locprim(ishell+1)= locprim(ishell)+1
          if(spher) then
            mbf(ishell)= 5
            locbf(ishell+1) = locbf(ishell)+5
          else
            mbf(ishell)= 6
            locbf(ishell+1) = locbf(ishell)+6
          endif
!  F function
          ishell= ishell+1
          do j= 1,1
            ex(locprim(ishell)+j)= expf(ife(numatomic(iatom))+j)
            coeff(locprim(ishell)+j)= one
          enddo
          mprim(ishell)= 1
          mtype(ishell)= 3
          locatom(ishell)= iatom
          locprim(ishell+1)= locprim(ishell)+1
          if(spher) then
            mbf(ishell)= 7
            locbf(ishell+1) = locbf(ishell)+7
          else
            mbf(ishell)= 10
            locbf(ishell+1) = locbf(ishell)+10
          endif
      endselect
!
      if(ishell > mxshell) then
        write(iout,'(" Error! The number of basis shells exceeds mxshel",i6,".")')mxshell
        call iabort
      endif
      if(locprim(ishell+1) > mxprim ) then
        write(iout,'(" Error! The number of primitive basis functions exceeds mxshel", &
&             i6,".")')mxprim
        call iabort
      endif
      if(locbf(ishell+1) > mxao ) then
        write(iout,'(" Error! The number of basis functions exceeds mxshel",i6,".")')mxao
        call iabort
      endif
      return
end


