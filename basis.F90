      call bs631g(iatom,ishell)
!
      select case (numatomic(iatom))
        case(3:18)
          ishell= ishell+1
          ex(locprim(ishell)+1)= ex631gs(numatomic(iatom))
          coeff(locprim(ishell)+1)= one
          mprim(ishell)= 1
          mtype(ishell)= 2
          locatom(ishell)= iatom
          locprim(ishell+1)= locprim(ishell)+1
          if(spher) then
            mbf(ishell)= 5
